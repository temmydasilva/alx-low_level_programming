#include <stdio.h>

/**
 * add - add two integers
 * @a: first int
 * @b: second int
 * Return: addition
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two integers
 * @a: first int
 * @b: second int
 * Return: subtracted final result
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two integers
 * @a: first int
 * @b: second int
 * Return: result of multiplied integers
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - mod two integers
 * @a: first int
 * @b: second int
 * Return: final result
 */

int mod(int a, int b)
{
	return (a % b);
}

/**
 * div - divide two integers
 * @a: first int
 * @b: second int
 * Return: result of divided integers
 */

int div(int a, int b)
{
	return (a / b);
}
